public class DataTypes {
  public static void main(String[] args) {
    int lucky_number = 7; // Integer (whole number)
    float floatingNumber = 19.99f; // Floating point number
    char lonelyLetter = 'O'; // Character
    boolean honestBoolean = true; // Boolean
    String badJoke = "Just laugh u don't need it"; // String
    System.out.println(badJoke);
  }
}

/*
 * Datatypes in Java :
 * 
 * 1 .Whole-Value-Datatypes :
 *    byte: 1 byte size
 *    short: 2 bytes size
 *    int: 4 bytes size
 *    long: 8 bytes size
 * 
 * 2 .Decimal Values :
 *    float: 4 bytes size.
 *    double: 8 bytes size
 *    + A float value always has the f-suffix. For example: 15.5f
 * 
 * 3 .Other types of data :
 *    char: a single character. 2 bytes size. 
 *    boolean: true or false. 1 byte. 
 *    String: a String.
 * 
 * 4 .Using a datatype :
 *    String name = "Max";
 *    int age = 24;
 * 
 * 5  .Casting a datatype :
 *    double value = 5.9;
 *    System.out.println((int) value);  // 5
 * 
 *   + With casting, we can transform data from one type to another type. When transforming a decimal value to an integer, Java will always round off.
 */
